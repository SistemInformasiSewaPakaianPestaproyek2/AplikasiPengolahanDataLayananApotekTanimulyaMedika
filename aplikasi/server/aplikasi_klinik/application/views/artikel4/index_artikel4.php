 Kadar HDL yang tinggi dipercaya dapat melindungi Anda terhadap kejadian penyakit jantung.Kadar kolesterol yang tinggi di dalam darah akan menyebabkan kolesterol tersebut menempel di dinding bagian dalam pembuluh darah arteri dan membentuk ateroma atau plak, sehingga pembuluh darah menyempit dan akhirnya menimbulkan serangan jantung jika menghambat pembuluh darah menuju jantung atau stroke jika menghambat pembuluh darah arteri yang menuju otak.</p>
		
    
        
