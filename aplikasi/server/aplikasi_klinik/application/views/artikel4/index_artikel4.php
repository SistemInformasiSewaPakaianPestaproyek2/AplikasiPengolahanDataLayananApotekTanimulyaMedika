		  <p>Kolesterol tidak dapat larut dalam darah sehingga membutuhkan ‘carrier’ atau protein pembawa yang membawa kolesterol ke seluruh tubuh. Protein pembawa disebut apolipoprotein/ apoprotein (apo). Kesatuan antara kolesterol dan apolipoprotein itu dikenal sebagai lipoprotein dan lipoprotein yang paling banyak dikenal, yaitu:
</p>
		  <p><b>1. LDL (Low Density Lipoprotein)</b>
	      LDL seringkali disebut ‘kolesterol jahat’ karena dapat menempel di dinding pembuluh darah dan mempersempit rongga pembuluh darah. Mengonsumsi makanan kaya lemak jenuh berlebihan dapat meningkatkan kadar LDL. Kadar LDL yang tinggi meningkatkan risiko Anda terhadap kejadian penyakit jantung. </p>
		  <p><b>2. HDL (High Density Lipoprotein)</b>
		    HDL seringkali disebut sebagai ‘koleterol baik’ karena bertugas untuk membawa kelebihan kolesterol yang tertinggal di pembuluh darah lalu membawanya ke hati untuk diproses lebih lanjut. </p>
		  </th>
