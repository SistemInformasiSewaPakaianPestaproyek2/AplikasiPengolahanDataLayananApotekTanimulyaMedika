		  <p>Ginjal mengemban tugas berat dalam tubuh, namun terkadang kurang mendapatkan perhatian. Ginjal merupakan salah satu organ vital tubuh, kerusakan pada ginjal akan menyebabkan turunnya kualitas kesehatan secara menyeluruh.
Kerusakan atau penyakit ginjal ibarat buah apel yang telah busuk di dalam, tapi tampak segar dari luar. Di awal penyakit tidak bergejala, namun tahu-tahu divonis harus melakukan cuci darah. Itulah makanya penyakit ginjal mendapatkan julukan sebagai silent disease.
Banyak orang yang tidak terlalu peduli dengan kesehatan ginjalnya. Pada kondisi sudah divonis cuci darah, barulah sibuk mencari-cari informasi mengenal penyakit ginjal. Padahal, bila sudah divonis cuci darah, maka tidak ada pilihan tindakan lain yang bisa diperbuat.
	      Oleh sebab itu, memelihara kesehatan ginjal sebelum munculnya gangguan ginjal patut dilakukan, salah satuna melalui skrining secara rutin tanpa menunggu datangnya keluhan.
Ginjal berfungis untuk mengeluarkan racun sisa metabolisme dalam tubuh. Apabila ginjal mengalami kerusakan, maka sisa metabolisme akan menumpuk di dalam tubuh dan meracuni tubuh kita.
 </p>
